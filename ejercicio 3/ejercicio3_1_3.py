class Calculadora:
    def __init__(self):
        pass

    def sumar(self, valor1, valor2):
        return valor1 + valor2

    def restar(self, valor1, valor2):
        return valor1 - valor2

    def multiplicar(self, valor1, valor2):
        return valor1 * valor2

    def dividir(self, valor1, valor2):
        return valor1 / valor2


